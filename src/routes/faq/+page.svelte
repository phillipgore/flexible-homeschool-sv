            <h2 class="fnt-weight-500 m-tn-t-0 m-tn-b-9">How do I provide instructions to my students?</h2>
            <p>There are two places to provide instructions and one place to record what you actually accomplished. Clarifying their roles in the app should help with planning and tracking.</p>
            <h3>General Instructions <span class="cl-gray-darkest fnt-size-tn-16 fnt-weight-normal">(Planning Section: Inserting or Updating School Work)</span></h3>
            <p>When planning, do not focus on specifics (See the question: <a class="cl-success" href="#breaks">How do I handle vacations, unforeseen breaks or sickness?</a>). The General Instructions for an item of School Work are loose goals and notes. If you plan to do 3 pages a segment, and one of those days you do a test instead, that’s not a problem; the app will flex around it.</p>
            
            <h3>Notes <span class="cl-gray-darkest fnt-size-tn-16 fnt-weight-normal">(Tracking Section: Notes Dropdown)</span></h3>
            <p>As you work your way through the school year, you may want to make note (specifics instructions or changes) about an item of School Work for that week. You can do so by pressing the Notes button next to the item of School Work in the Tracking section. If notes are present, the button will turn orange to alert students that a note has been added.</p>
            
            <h3>Completed Work <span class="cl-gray-darkest fnt-size-tn-16 fnt-weight-normal">(Tracking Section: Editing a Segment: Segment Dropdown)</span></h3>
            <p>Once you have completed the work for a segment, you can record exactly what you accomplished in the Segments completed work text area.</p>
