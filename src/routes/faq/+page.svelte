            <h2 class="fnt-weight-500 m-tn-t-0 m-tn-b-9">Can I print a weekly checklist for my kids?</h2>
            <p>Yes. Create a new report. Give the report a meaningful name like “Weekly School Work.” The video, <a class="cl-success" href="https://www.aflexiblehomeschool.com/tutorials/reporting-workflow">Reporting Workflow Tutorial</a>, covers how to create a report.</p>
            <p>Because this report will focus on School Work, turn off the School Year section. The Terms and Resources sections are already unchecked by default on new Reports.</p>
            <p>That leaves you with the School Work section. Uncheck: Progress Bar, Time Statistics and Schedule Statistics. These items are not pertinent to helping a Student see what is expected that week.</p>
            <p>That leaves you with General Instructions, which will let the Student know what is expected with each item of School Work; and Attached Resource, which will let the Student know what Resources are to be used.</p>
            <p>You also want to turn on Weekly Data. This will show each Segment scheduled and its related information. Turn off the progress bar for Weekly Data, and click the “Save” button.</p>
            <p>Now you can choose the Student, Year, Term and Week you want and have a printable report of what’s expected for that week.</p>
