            <h2 class="fnt-weight-500 m-tn-t-0 m-tn-b-9">How do I schedule field trips or special activities?</h2>
            <p>The best way to handle a special field trip is to treat it as its own School Work Item. If it’s going to be 3-day trip, schedule it for 3 Segments; if 4 days, schedule it for 4 Segments.</p>
            <p>When planning, you won’t know how far into the Term the field trip will happen because the app uses flexible timeframes, not specific dates on a calendar (See the question: <a class="cl-success" href="#breaks">How do I handle vacations, unforeseen breaks or sickness?</a>). So I would schedule it for the week you think it might fall on or for the last week of the term. </p>
            <p>If you want to record the exact dates of the trip or what you did each day, you can do so in each segment’s dropdown in the Tracking Section.</p>
