            <h2 class="fnt-weight-500 m-tn-t-0 m-tn-b-9">How do I handle vacations, unforeseen breaks, or sickness?</h2>
            <p>When Scheduling your School Year, it’s best not to think about when school is going to happen. Rather you should focus on what needs to happen for your School Year to be complete.</p>
            <p>For instance, don’t think about when you will start or when you will take breaks or what calendar week your vacation falls on. The app uses flexible timeframes instead of specific dates on a calendar. You can start or stop whenever, and the app will flex around it without any extra effort.</p>
            <div class="well well-primary p-tn-9 m-tn-b-18 m-sm-h-27 m-md-h-54 m-lg-h-72">
                <div class="well-icon"><i class="fss-info"></i></div>
                <div class="well-message">
                    <p class="fnt-weight-bold">What do you mean by flexible timeframes?</p>
                    <p><span class="fnt-weight-bold">School Year:</span> The time necessary to complete one year of scheduled School Work, not a specific period of 12 consecutive months.</p>
                    <p><span class="fnt-weight-bold">Term:</span> The time necessary to complete one term of scheduled School Work, not a specific set of dates on a calendar.</p>
                    <p><span class="fnt-weight-bold">Week:</span> The time necessary to complete one week of scheduled School Work, not a specific period of 7 (or 5) consecutive days.</p>
                    <p class="m-tn-b-0"><span class="fnt-weight-bold">Segment:</span> The time necessary to complete one day of scheduled School Work, not a specific day on a calendar.</p>
                </div>
            </div>
            <p>Instead of focusing on the calendar dates on which school will or will not happen, focus on what you need to accomplish over the course of the School Year. For instance: I will complete 3 Terms of 12 weeks each for a total of 36 weeks. Or: I will complete 4 terms of 9 weeks each for a total of 36 weeks.</p>
            <p>Because the app uses flexible timeframes instead of specific dates on the calendar, you can speed up, slow down, stop or start as needed without having to mess with the app. </p>
            
