            <h2 class="fnt-weight-500 m-tn-t-0 m-tn-b-9">If breaks are unimportant, why divide the year into Terms?</h2>
            <p>You can just schedule one long term of 36 weeks, but for purely psychological reasons, that can feel like a lot in one big chunk. So it’s helpful to divide the School Year into Terms.</p>
            <p>For instance, my family does 3 terms of 12 weeks each for a total of 36 weeks. You might choose to do 4 terms of 9 weeks for a total of 36 weeks. It’s really up to you how you wish to organize your School Year.</p>
            <p>The other reason to divide the year into Terms might be to change up what School Work is being done for each Term. For instance, our 14-year-old only did Geology School Work the First Term this last year. We could have just scheduled Geology for the first 12 weeks of one 36 week term, but it’s simpler to break up the year.</p>
            <p><span class="fnt-weight-bold">Bottom Line:</span> Terms help break up the School Year mentally. It’s just easier to focus on getting through 12 weeks of School (1st term), celebrate the accomplishment, and then dive into the next 12 weeks (2nd term).</p>
